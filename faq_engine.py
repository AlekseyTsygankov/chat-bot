def load_faq():
    """–ó–∞–≥—Ä—É–∑–∏—Ç—å FAQ –∏–∑ —Ñ–∞–π–ª–∞ –∏–ª–∏ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω—É—é –±–∞–∑—É"""
    faq_path = Path(__file__).parent.parent / "data" / "faq.json"
    
    if faq_path.exists():
        try:
            with open(faq_path, 'r', encoding='utf-8') as f:
                return json.load(f)
        except Exception as e:
            logger.warning(f"–ù–µ —É–¥–∞–ª–æ—Å—å –∑–∞–≥—Ä—É–∑–∏—Ç—å FAQ –∏–∑ {faq_path}: {e}")
            return DEFAULT_FAQ
    
    logger.info("–ò—Å–ø–æ–ª—å–∑—É–µ—Ç—Å—è —Å—Ç–∞–Ω–¥–∞—Ä—Ç–Ω–∞—è –±–∞–∑–∞ FAQ")
    return DEFAULT_FAQ

def normalize_text(text: str) -> str:
    """–ù–æ—Ä–º–∞–ª–∏–∑–æ–≤–∞—Ç—å —Ç–µ–∫—Å—Ç –¥–ª—è –ø–æ–∏—Å–∫–∞"""
    return text.lower().strip()

def calculate_similarity(text1: str, text2: str) -> float:
    """–í—ã—á–∏—Å–ª–∏—Ç—å —Å—Ö–æ–∂–µ—Å—Ç—å –¥–≤—É—Ö —Å—Ç—Ä–æ–∫ (0-1)"""
    text1 = normalize_text(text1)
    text2 = normalize_text(text2)
    return SequenceMatcher(None, text1, text2).ratio()

def find_answer(user_query: str, threshold: float = 0.5) -> dict:
    """
    –ù–∞–π—Ç–∏ –æ—Ç–≤–µ—Ç –≤ FAQ –ø–æ –∑–∞–ø—Ä–æ—Å—É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    
    Args:
        user_query: –í–æ–ø—Ä–æ—Å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
        threshold: –ú–∏–Ω–∏–º–∞–ª—å–Ω—ã–π –ø–æ—Ä–æ–≥ —Å—Ö–æ–¥—Å—Ç–≤–∞ (0-1)
    
    Returns:
        dict —Å —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–º –ø–æ–∏—Å–∫–∞
    """
    faq = load_faq()
    best_match = None
    best_score = 0
    
    # –ü–æ–∏—Å–∫ –ø–æ –≤—Å–µ–º –∫–∞—Ç–µ–≥–æ—Ä–∏—è–º
    for category_data in faq:
        category = category_data['category']
        
        for q_data in category_data.get('questions', []):
            question = q_data['question']
            
            # –í—ã—á–∏—Å–ª–∏—Ç—å —Å—Ö–æ–¥—Å—Ç–≤–æ
            similarity = calculate_similarity(user_query, question)
            
            if similarity > best_score:
                best_score = similarity
                best_match = {
                    'category': category,
                    'question': question,
                    'answer': q_data['answer'],
                    'similarity_score': similarity
                }
    
    # –ü—Ä–æ–≤–µ—Ä–∏—Ç—å –ø–æ—Ä–æ–≥–æ–≤–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ
    if best_match and best_score >= threshold:
        return {
            'found': True,
            'category': best_match['category'],
            'question': best_match['question'],
            'answer': best_match['answer'],
            'similarity_score': best_score
        }
    else:
        return {
            'found': False,
            'category': None,
            'question': None,
            'answer': None,
            'similarity_score': best_score
        }

def get_categories() -> list:
    """–ü–æ–ª—É—á–∏—Ç—å —Å–ø–∏—Å–æ–∫ –≤—Å–µ—Ö –∫–∞—Ç–µ–≥–æ—Ä–∏–π"""
    faq = load_faq()
    return [cat['category'] for cat in faq]
